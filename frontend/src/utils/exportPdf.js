import html2pdf from 'html2pdf.js';

/**
 * Generate a PDF from an HTML string rendered inside an iframe.
 * Uses INLINE STYLES only because html2canvas ignores <style> tags.
 */
export const exportToPdf = async (htmlContent, filename = 'document.pdf') => {
  const iframe = document.createElement('iframe');
  iframe.style.position = 'fixed';
  iframe.style.top = '0';
  iframe.style.left = '0';
  iframe.style.width = '800px';
  iframe.style.height = '100%';
  iframe.style.zIndex = '-1';
  iframe.style.opacity = '0';
  document.body.appendChild(iframe);

  const iframeDoc = iframe.contentDocument || iframe.contentWindow.document;
  iframeDoc.open();
  iframeDoc.write(`<!DOCTYPE html>
<html>
<head><meta charset="utf-8" /></head>
<body style="font-family: 'Segoe UI', Tahoma, Geneva, Verdana, sans-serif; color: #1f2937; background: #fff; padding: 32px 38px; font-size: 12.5px; line-height: 1.8; -webkit-print-color-adjust: exact; print-color-adjust: exact;">
  ${htmlContent}
  <div style="margin-top: 36px; padding-top: 12px; border-top: 1px solid #e5e7eb; text-align: center; font-size: 9px; color: #9ca3af; letter-spacing: 0.3px;">
    Generated by UniAct Knowledge System &bull; ${new Date().toLocaleDateString()}
  </div>
</body>
</html>`);
  iframeDoc.close();

  await new Promise(resolve => setTimeout(resolve, 600));

  const opt = {
    margin:       [10, 10, 10, 10],
    filename:     filename,
    image:        { type: 'jpeg', quality: 0.98 },
    html2canvas:  { scale: 2, useCORS: true },
    jsPDF:        { unit: 'mm', format: 'a4', orientation: 'portrait' },
    pagebreak:    { mode: ['css', 'legacy'] }
  };

  try {
    await html2pdf().set(opt).from(iframeDoc.body).save();
  } catch (err) {
    console.error('PDF Export Error:', err);
  } finally {
    document.body.removeChild(iframe);
  }
};

