/** Document header block (exam, summary, mindmap) */
export function buildHeader(title, metaLines) {
  return `
    <div style="text-align:center; padding:18px 24px 16px; margin-bottom:28px; background:#f0f0ff; border:1px solid #ddd5f5; border-radius:8px; border-top:4px solid #6366f1;">
      <div style="font-size:20px; font-weight:700; color:#1e1b4b; margin-bottom:10px;">${title}</div>
      <div style="font-size:11px; color:#6b7280; line-height:1.7;">${metaLines}</div>
    </div>`;
}

